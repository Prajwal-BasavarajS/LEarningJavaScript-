let n1=9;
let n2=33;
var sym = "/";

switch(sym){
    case "+":
        console.log(n1+n2);
        break;
    case "-":
    console.log(n1-n2);
    break;
    case "*":
        console.log(n1*n2);
        break;
    case "/":
        console.log(n1/n2);
        break;
    default:
        console.log("invalid symbol");
}
